package com.example.soccerteammanager;

public class Match {

    private String homeTeam;
    private String awayTeam;
    private String score;

    Match(String homeTeam, String awayTeam, String score){
        this.homeTeam = homeTeam;
        this.awayTeam = awayTeam;
        this.score = score;
    }

}
